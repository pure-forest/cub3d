int	ft_strcmp(const char *s1, const char *s2)
{
	int				dif;

	while (1)
	{
		if (!*s1 && !*s2)
			return (0);
		dif = *s1 - *s2;
		if (dif)
			return (dif);
		s1++;
		s2++;
	}
}
